function countProfit(shoppers) {
    let listBarang = [ ['Sepatu Stacattu', 1500000, 10],
                       ['Baju Zoro', 500000, 2],
                       ['Sweater Uniklooh', 175000, 1]
                     ];
  
    var output = [];

    // check if shoppers is filled in
    if (!shoppers.length) {
        return output
    }
    
    // Prepare output
    for (var i = 0; i < listBarang.length; i++) {
        output.push({
            product : listBarang[i][0],
            shoppers : [],
            leftOver: listBarang[i][2],
            totalProfit: 0
        })
    }
    
    // find if product bought exist and has enough in stock
    for (var i = 0; i < shoppers.length; i++) {
        for (var j = 0; j < output.length; j++) {
            if (shoppers[i].product === output[j].product && shoppers[i].amount <= output[j].leftOver) {
                // reduce amount of leftOver by amount bought
                output[j].leftOver -= shoppers[i].amount;

                // push shopper name into list of shoppers for this product
                output[j].shoppers.push(shoppers[i].name)

                // calculate Totalprofit (original stock - leftOver) * price
                output[j].totalProfit = (listBarang[j][2] - output[j].leftOver) * listBarang[j][1];
            }
        }
    }
    
    return output
  }
  
